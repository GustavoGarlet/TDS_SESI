function somar(a, b) {
    return a + b;
}
function subracao(a, b) {
    return a - b;
}
function multiplicacao(a, b) {
    return a * b;
}
function divisao(a, b) {
    return a / b;
}
function IMC(peso, altura) {
    return peso / (altura * altura);
}

console.log(somar(5, 7));
console.log(subracao(30, 10));
console.log(multiplicacao(5, 7));
console.log(divisao(14, 7));
console.log(IMC(56, 1.4));
